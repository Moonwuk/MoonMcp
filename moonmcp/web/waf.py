"""WAF / CDN fingerprinting (a compact, wafw00f-style heuristic).

Inspects response headers, cookies and server strings from a normal request, and
optionally sends a benign "suspicious" request to see whether a protective layer
trips (403/406/429 + a block signature).  Standard-library only.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from urllib.parse import quote

from ..net.http import HttpClient
from .waf_bypass import _BLOCK_SIGNS  # shared block-page body signatures

